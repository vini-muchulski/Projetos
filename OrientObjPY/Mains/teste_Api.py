import requests

class BuscaEndereco:

    def __init__(self, cep):
        cep = str(cep)
        if self.cep_eh_valido(cep):
            self.cep = cep

        else:
            raise ValueError("CEP Inv√°lido")

    def __str__(self):
        return self.mascara_cep()

    def cep_eh_valido(self,cep):
        if len(cep) == 8:
            return True
        else:
            return False

    def mascara_cep(self):
        cep_formatado = "{}-{}".format(self.cep[:5],self.cep[5:8])
        cep_formatado = str(cep_formatado)
        return cep_formatado

    def acesso_cep(self):
      url =requests.get("https://viacep.com.br/ws/{}/json/".format(self.cep))
      #return url.text
      dados = url.json()
      return ( dados['uf'], dados['localidade'], dados['bairro'], dados['complemento'])

